    function initializeUserProfile() {
        const savedName = localStorage.getItem('haytham_name');
        if (savedName) {
            profileNameInput.value = savedName;
            postUsername.textContent = savedName;
            postWatermark.textContent = savedName;
        }

        const savedTitle = localStorage.getItem('haytham_title');
        if (savedTitle) {
            profileTitleInput.value = savedTitle;
            postUserTitle.textContent = savedTitle;
        }

        const savedVerified = localStorage.getItem('haytham_verified');
        if (savedVerified !== null) {
            const isVerified = savedVerified === 'true';
            verifiedToggleDaily.checked = isVerified;
            verificationBadge.style.display = isVerified ? 'block' : 'none';
        }

        const savedAvatar = localStorage.getItem('haytham_avatar_data');
        if (savedAvatar) {
            avatarPreviewDaily.src = savedAvatar;
            postProfileImg.src = savedAvatar;
        }
    }

    // ==========================================
    // 11. توليد جسيمات النيون الرقمية المتطايرة في الخلفية
    // ==========================================
    function generateBackgroundParticles() {
        const container = document.getElementById('particles-container');
        if (!container) return;

        const maxParticles = window.innerWidth <= 768 ? 20 : 40;
        
        for (let i = 0; i < maxParticles; i++) {
            const particle = document.createElement('div');
            
            // تحديد الحجم العشوائي وموقع البداية
            const size = Math.random() * 5 + 2;
            const delay = Math.random() * 10;
            const duration = Math.random() * 15 + 10;
            const startX = Math.random() * 100;
            const startY = Math.random() * 100;
            
            // تلوين عشوائي بين النيون والأزرق والذهبي
            const colorType = Math.random();
            let color = 'rgba(0, 212, 255, 0.4)'; // أزرق نيون
            if (colorType > 0.7) color = 'rgba(255, 215, 0, 0.4)'; // ذهبي
            
            Object.assign(particle.style, {
                position: 'absolute',
                width: `${size}px`,
                height: `${size}px`,
                borderRadius: '50%',
                background: color,
                top: `${startY}%`,
                left: `${startX}%`,
                opacity: Math.random() * 0.5 + 0.1,
                boxShadow: `0 0 10px ${color}`,
                pointerEvents: 'none',
                animation: `floatParticle ${duration}s linear infinite`,
                animationDelay: `${delay}s`
            });
            
            container.appendChild(particle);
        }

        // إضافة حركة الطفو للجسيمات في التنسيقات العامة
        const style = document.createElement('style');
        style.textContent = `
            @keyframes floatParticle {
                0% { transform: translateY(0) translateX(0); }
                50% { transform: translateY(-50px) translateX(20px); }
                100% { transform: translateY(-100px) translateX(0); opacity: 0; }
            }
        `;
        document.head.appendChild(style);
    }

