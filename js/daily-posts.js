            let dotClass = 'dot-teaching';
            if (dayInfo.pillar === 'tips') dotClass = 'dot-tips';
            if (dayInfo.pillar === 'trend') dotClass = 'dot-trend';
            if (dayInfo.pillar === 'story') dotClass = 'dot-story';
            if (dayInfo.pillar === 'sell') dotClass = 'dot-sell';

            // تحديد الرمز التعبيري المصاحب لنوع المحتوى
            let typeIcon = '🎬';
            if (dayInfo.type.includes('كاروسيل')) typeIcon = '🗂️';
            if (dayInfo.type.includes('استطلاع')) typeIcon = '📊';
            if (dayInfo.type.includes('سؤال')) typeIcon = '❓';
            if (dayInfo.type.includes('صورة')) typeIcon = '📸';

            dayCell.innerHTML = `
                <span class="day-num">${i}</span>
                <span class="day-type-icon">${typeIcon}</span>
                <span class="legend-dot ${dotClass}" style="position: absolute; bottom: 6px; left: 50%; transform: translateX(-50%);"></span>
            `;

            // إضافة تفاصيل للمساعدة عند التمرير بالماوس
            dayCell.title = `اليوم ${i} | ${dayInfo.type} | ${dayInfo.pillarName}`;

