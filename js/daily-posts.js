    // دالة لتحديث الواجهة بناءً على كائن البيانات لليوم الحالي
    function renderDayData(dayInfo) {
        // تحديث معلومات شريط التحكم العلوي
        daySelector.value = dayInfo.day;
        phaseIndicator.textContent = getPhaseText(dayInfo.day);
        pillarBadge.textContent = dayInfo.pillarName;
        postTypeBadge.textContent = dayInfo.type;
        kpiDisplay.textContent = dayInfo.kpi;
        visualDesc.textContent = dayInfo.visualDesc;
        
        // تحديث التاريخ
        updateDateAndWeekday(dayInfo.day);

        // تحديث معلومات رأس المعاينة للبوست
        dayTitle.textContent = `اليوم ${dayInfo.day} | ${dayInfo.type} | ${dayInfo.pillarName.split(' ')[1]}`;
        daySubtitle.textContent = getPhaseText(dayInfo.day);

        // تحديث بطاقة البوست المعروضة (Instagram Post Preview)
        postTypeTag.textContent = dayInfo.type;
        postHook.textContent = dayInfo.hook;
        
        // تحديث جسم البوست وتحويل المصفوفة لسطور HTML
        postBody.innerHTML = '';
        dayInfo.body.forEach(line => {
            const p = document.createElement('p');
            p.textContent = line;
            postBody.appendChild(p);
        });

        postCta.textContent = dayInfo.cta;

        const hashtags = generateHashtags(dayInfo.pillar);
        postHashtags.textContent = hashtags;

        // تحديث صندوق التصدير والنسخ للكابشن والهاشتاقات معاً
        const fullCaption = `${dayInfo.hook}\n\n${dayInfo.body.join('\n')}\n\n${dayInfo.cta}\n\n${hashtags}`;
        captionOutput.value = fullCaption;

        // تحديث شريط التقدم العام
        const percent = (dayInfo.day / 90) * 100;
        phaseProgress.style.width = `${percent}%`;
        overallProgressText.textContent = `اليوم ${dayInfo.day} من 90`;

        // تحديث مرحلة شريط التنقل العلوي
        let phaseNum = 1;
        if (dayInfo.day > 30 && dayInfo.day <= 60) phaseNum = 2;
        if (dayInfo.day > 60) phaseNum = 3;
        navPhaseBadge.querySelector('span').textContent = `المرحلة ${phaseNum}`;

        // إزالة الفئات النشطة للمراحل وتفعيل الحالية فقط
        document.querySelectorAll('.phase-label').forEach(label => label.classList.remove('active-phase-label'));
        if (dayInfo.day <= 30) {
            document.querySelector('.phase-1-label').classList.add('active-phase-label');
        } else if (dayInfo.day <= 60) {
            document.querySelector('.phase-2-label').classList.add('active-phase-label');
        } else {
            document.querySelector('.phase-3-label').classList.add('active-phase-label');
        }

        // تحديث الصف النشط في التقويم السفلي
        document.querySelectorAll('.calendar-day').forEach(dayCell => {
            dayCell.classList.remove('active-day');
            if (parseInt(dayCell.getAttribute('data-day')) === dayInfo.day) {
                dayCell.classList.add('active-day');
            }
        });
    }

    function getPhaseText(dayNum) {
        if (dayNum <= 30) return "المرحلة 1: بناء الثقة والوعي";
        if (dayNum <= 60) return "المرحلة 2: تفاعل المجتمع والجمهور";
        return "المرحلة 3: تحويل المتابعين إلى عملاء";
    }

    // دالة لتحديث اليوم النشط وجلب بياناته وعرضها
    function loadDay(dayNum) {
        if (dayNum < 1 || dayNum > 90) return;
        currentDay = dayNum;
        
        // التحقق من وجود محتوى تم توليده وتعديله مسبقاً في الجلسة المخزنة
        const sessionKey = `haytham_daily_day_${currentDay}`;
        const savedData = localStorage.getItem(sessionKey);
        
        if (savedData) {
            renderDayData(JSON.parse(savedData));
        } else {
            const defaultInfo = getDayContent(currentDay);
            renderDayData(defaultInfo);
        }
    }

